def _render_demographics(pdf: MedleyPDF, patient: PatientSession) -> None:
    pdf.section_heading("Patient Information")
    pdf.kv("Name", patient.name)
    pdf.kv("Age / Sex", f"{patient.age}y {patient.sex}")
    pdf.kv("Arrival", patient.arrival_time.strftime("%Y-%m-%d %H:%M"))
    _ap = patient.arrival_pathway
    pdf.kv("Pathway", (_get_val(_ap, "value", str(_ap))).replace("_", " ").title())
    pdf.kv("Chief Complaint", patient.speech_text)
    pdf.ln(3)


def _render_vitals(pdf: MedleyPDF, patient: PatientSession) -> None:
    fc = patient.full_context
    if not fc:
        return
    v = _get_val(fc, "vitals")
    if not v: return
    
    pdf.section_heading("Vital Signs")
    headers = ["HR (bpm)", "BP (mmHg)", "RR (/min)", "SpO2 (%)", "Temp (C)", "AVPU"]
    values = [
        str(_get_val(v, "heart_rate", "N/A")),
        f"{_get_val(v, 'systolic_bp', 'N/A')}/{_get_val(v, 'diastolic_bp', 'N/A')}",
        str(_get_val(v, "respiratory_rate", "N/A")),
        str(_get_val(v, "spo2", "N/A")),
        f"{float(_get_val(v, 'temperature', 0)):.1f}",
        _get_val(_get_val(v, "consciousness", "Alert"), "value", str(_get_val(v, "consciousness", "Alert"))),
    ]
    col_w = 30
    # Header row
    pdf.set_font("Helvetica", "B", 8)
    for h in headers:
        pdf.cell(col_w, 5, h, border=1, align="C")
    pdf.ln()
    # Value row
    pdf.set_font("Helvetica", "", 9)
    for val in values:
        pdf.cell(col_w, 6, val, border=1, align="C")
    pdf.ln(6)


def _render_asr(pdf: MedleyPDF, patient: PatientSession) -> None:
    asr = patient.asr_result
    if not asr:
        return
    pdf.section_heading("Speech Recognition (Dual-ASR)")
    pdf.kv("Confidence", f"{_get_val(asr, 'confidence_score', 0):.0%}")
    pdf.kv("Disagreements", str(_get_val(asr, 'disagreement_count', 0)))
    if _get_val(asr, 'has_critical_disagreements', False):
        pdf.alert_text("Critical ASR disagreements detected on clinically significant terms")
    
    disagreements = _get_val(asr, "disagreements", [])
    for d in disagreements:
        resolved = f" -> {_get_val(d, 'resolved_to')}" if _get_val(d, 'resolved') else " (unresolved)"
        pdf.set_font("Helvetica", "", 8)
        indent_x = pdf.l_margin + 4
        pdf.set_x(indent_x)
        old_lm = pdf.l_margin
        pdf.set_left_margin(indent_x)
        pdf.multi_cell(
            0, 5,
            f'MedASR: "{_get_val(d, "medasr_word")}" vs Whisper: "{_get_val(d, "whisper_word")}" '
            f'[{_get_val(d, "clinical_significance", "moderate")}]{resolved}',
        )
        pdf.set_left_margin(old_lm)
    pdf.ln(3)


def _render_ehr_risk_flags(pdf: MedleyPDF, patient: PatientSession) -> None:
    fc = patient.full_context
    if not fc: return
    ehr = _get_val(fc, "ehr")
    if not ehr: return
    
    risk_flags = _get_val(ehr, "risk_flags", [])
    if not risk_flags:
        return
    pdf.section_heading("EHR Risk Flags")
    for rf in risk_flags:
        pdf.set_font("Helvetica", "B", 9)
        sev_label = str(_get_val(rf, "severity", "moderate")).upper()
        indent_x = pdf.l_margin + 4
        pdf.set_x(indent_x)
        old_lm = pdf.l_margin
        pdf.set_left_margin(indent_x)
        pdf.multi_cell(
            0, 5,
            f"[{sev_label}] {_get_val(rf, 'flag_type')}: {_get_val(rf, 'description')}",
        )
        pdf.set_left_margin(old_lm)
    pdf.ln(3)


def _render_triage_result(pdf: MedleyPDF, patient: PatientSession) -> None:
    ta = patient.triage_agreement
    if not ta:
        return
    pdf.section_heading("Ensemble Triage Result")
    pdf.set_font("Helvetica", "", 10)
    pdf.cell(30, 7, "Final Level: ")
    _fl = _get_val(ta, "final_level")
    pdf.retts_badge(_get_val(_fl, "value", str(_fl)))
    pdf.ln(9)
    _al = _get_val(ta, "agreement_level")
    pdf.kv("Agreement", f"{_get_val(_al, 'value', str(_al))} ({_get_val(ta, 'agreement_ratio', 0):.0%})")
    pdf.kv("Total Voters", str(_get_val(ta, 'total_voters', 0)))

    # Vote distribution
    pdf.set_x(pdf.l_margin)
    pdf.set_font("Helvetica", "B", 9)
    pdf.cell(0, 6, "Vote Distribution:", new_x="LMARGIN", new_y="NEXT")
    vote_dist = _get_val(ta, "vote_distribution", {})
    for level_str, count in sorted(
        vote_dist.items(),
        key=lambda x: ["RED", "ORANGE", "YELLOW", "GREEN", "BLUE"].index(x[0])
        if x[0] in ["RED", "ORANGE", "YELLOW", "GREEN", "BLUE"] else 99,
    ):
        pdf.set_font("Helvetica", "", 9)
        pdf.cell(0, 5, f"  {level_str}: {count}/{_get_val(ta, 'total_voters', 1)}", new_x="LMARGIN", new_y="NEXT")

    esc_reason = _get_val(ta, "escalation_reason")
    if esc_reason:
        pdf.ln(2)
        pdf.alert_text(f"ESCALATION: {esc_reason}")

    if _get_val(ta, "requires_senior_review", False):
        pdf.alert_text("SENIOR REVIEW REQUIRED")

    dissenting = _get_val(ta, "dissenting_models", [])
    if dissenting:
        pdf.set_x(pdf.l_margin)
        pdf.set_font("Helvetica", "I", 8)
        pdf.multi_cell(0, 5, f"Dissenting: {', '.join(dissenting)}")

    dont_miss = _get_val(ta, "dont_miss_alerts", [])
    if dont_miss:
        pdf.ln(2)
        pdf.set_x(pdf.l_margin)
        pdf.set_font("Helvetica", "B", 9)
        pdf.cell(0, 5, "Don't-Miss Alerts:", new_x="LMARGIN", new_y="NEXT")
        for alert in dont_miss:
            pdf.bullet(alert)
    pdf.ln(3)


def _render_cross_engine(pdf: MedleyPDF, patient: PatientSession) -> None:
    te = patient.triage_ensemble
    if not te: return
    engine_outs = _get_val(te, "engine_outputs", [])
    if len(engine_outs) < 2:
        return
    from src.engines.agreement_engine import analyze_engine_disagreement
    ed = analyze_engine_disagreement(engine_outs)
    if not ed:
        return
    pdf.section_heading("Cross-Engine Comparison")
    engine_lvls = _get_val(ed, "engine_levels", {})
    for engine_name, level in engine_lvls.items():
        pdf.kv(engine_name, level, bold_value=True)
    if not _get_val(ed, "engines_agree", True):
        pdf.ln(1)
        pdf.set_x(10)
        pdf.set_font("Helvetica", "I", 8)
        pdf.multi_cell(0, 4, _get_val(ed, "clinical_action", ""))
    pdf.ln(3)


def _render_nurse_override(pdf: MedleyPDF, patient: PatientSession) -> None:
    if not patient.overrides:
        return
    pdf.section_heading("Nurse Override")
    last = patient.overrides[-1]
    pdf.kv("From", str(last.get("original", "N/A")))
    pdf.kv("To", str(last.get("override_to", "N/A")))
    pdf.kv("Reason", str(last.get("reason", "Not specified")))
    pdf.ln(3)


def _render_management_category(pdf: MedleyPDF, heading: str, items: list[str]) -> None:
    """Render a management category (consensus items)."""
    pdf.set_font("Helvetica", "B", 9)
    pdf.cell(0, 6, f"{heading}:", new_x="LMARGIN", new_y="NEXT")
    if items:
        for item in items:
            pdf.bullet(item)
    else:
        pdf.set_font("Helvetica", "I", 8)
        pdf.set_text_color(*_MUTED_RGB)
        pdf.cell(0, 5, "  No consensus items", new_x="LMARGIN", new_y="NEXT")
        pdf.set_text_color(*_BLACK)


def _render_individual_model_items(pdf: MedleyPDF, patient: PatientSession, promoted: bool = False) -> None:
    """Render non-consensus items from individual models."""
    ma = patient.management_agreement
    me = patient.management_ensemble
    if not ma or not me:
        return

    common_inv = _get_val(ma, "common_investigations", [])
    common_img = _get_val(ma, "common_imaging", [])
    common_med = _get_val(ma, "common_medications", [])

    consensus_inv = {str(x).lower() for x in common_inv}
    consensus_img = {str(x).lower() for x in common_img}
    consensus_med = {str(x).lower() for x in common_med}

    extra_inv: list[tuple[str, str]] = []
    extra_img: list[tuple[str, str]] = []
    extra_med: list[tuple[str, str]] = []

    model_outputs = _get_val(me, "model_outputs", [])
    for output in model_outputs:
        mid = _get_val(output, "model_id", "AI")
        for item in _get_val(output, "investigations", []):
            if str(item).lower() not in consensus_inv:
                extra_inv.append((item, mid))
        for item in _get_val(output, "imaging", []):
            if str(item).lower() not in consensus_img:
                extra_img.append((item, mid))
        for item in _get_val(output, "medications", []):
            if str(item).lower() not in consensus_med:
                extra_med.append((item, mid))

    has_extras = extra_inv or extra_img or extra_med
    if not has_extras:
        return

    pdf.ln(2)

    if promoted:
        # Show per-model breakdown as primary content
        for output in model_outputs:
            inv = _get_val(output, "investigations", [])
            img = _get_val(output, "imaging", [])
            med = _get_val(output, "medications", [])
            if not inv and not img and not med:
                continue
            pdf.set_font("Helvetica", "B", 9)
            pdf.set_text_color(*_HEADER_RGB)
            pdf.cell(0, 6, f"{_get_val(output, 'model_id', 'AI')}:", new_x="LMARGIN", new_y="NEXT")
            pdf.set_text_color(*_BLACK)
            if inv:
                pdf.set_font("Helvetica", "I", 8)
                pdf.cell(0, 5, "  Investigations:", new_x="LMARGIN", new_y="NEXT")
                for item in inv:
                    pdf.bullet(item, indent=8)
            if img:
                pdf.set_font("Helvetica", "I", 8)
                pdf.cell(0, 5, "  Imaging:", new_x="LMARGIN", new_y="NEXT")
                for item in img:
                    pdf.bullet(item, indent=8)
            if med:
                pdf.set_font("Helvetica", "I", 8)
                pdf.cell(0, 5, "  Medications:", new_x="LMARGIN", new_y="NEXT")
                for item in med:
                    pdf.bullet(item, indent=8)
            pdf.ln(2)
        return

    pdf.set_font("Helvetica", "B", 9)
    pdf.set_text_color(*_SECONDARY_RGB)
    pdf.cell(0, 6, "Additional Items (Individual Models):", new_x="LMARGIN", new_y="NEXT")
    pdf.set_text_color(*_BLACK)

    seen: set[str] = set()
    for label, items_list in [("Investigations", extra_inv), ("Imaging", extra_img), ("Medications", extra_med)]:
        unique = []
        for item, model in items_list:
            if str(item).lower() not in seen:
                seen.add(str(item).lower())
                unique.append((item, model))
        if unique:
            pdf.set_font("Helvetica", "I", 8)
            pdf.cell(0, 5, f"  {label}:", new_x="LMARGIN", new_y="NEXT")
            for item, model in unique:
                pdf.set_font("Helvetica", "", 8)
                item_x = pdf.l_margin + 8
                pdf.set_x(item_x)
                old_lm = pdf.l_margin
                pdf.set_left_margin(item_x + 6)  # Wrap indent past "- "
                pdf.multi_cell(0, 5, f"- {item} ({model})")
                pdf.set_left_margin(old_lm)


def _render_approved_list(pdf: MedleyPDF, heading: str, items: list[str]) -> None:
    pdf.set_font("Helvetica", "B", 9)
    pdf.cell(0, 6, f"{heading}:", new_x="LMARGIN", new_y="NEXT")
    if items:
        for item in items:
            pdf.bullet(item)
    else:
        pdf.set_font("Helvetica", "I", 8)
        pdf.cell(0, 5, "  None specified", new_x="LMARGIN", new_y="NEXT")


