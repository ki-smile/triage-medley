# --------------------------------------------------------------------------- #
# Color constants (RGB tuples)
# --------------------------------------------------------------------------- #

_HEADER_RGB = (79, 4, 51)       # KI Purple #4F0433
_SECONDARY_RGB = (135, 0, 82)   # KI Secondary #870052
_MUTED_RGB = (102, 102, 102)    # Gray text
_BLACK = (0, 0, 0)
_WHITE = (255, 255, 255)
_ERROR_RGB = (184, 65, 69)      # Red for alerts

# Unicode → latin-1 safe replacements for built-in Helvetica font
_UNICODE_REPLACEMENTS = {
    "\u2014": "-",   # em-dash
    "\u2013": "-",   # en-dash
    "\u2018": "'",   # left single quote
    "\u2019": "'",   # right single quote
    "\u201c": '"',   # left double quote
    "\u201d": '"',   # right double quote
    "\u2026": "...", # ellipsis
    "\u2192": "->",  # right arrow
    "\u2190": "<-",  # left arrow
    "\u2265": ">=",  # greater-than-or-equal
    "\u2264": "<=",  # less-than-or-equal
    "\u00b7": "-",   # middle dot
}


def _sanitize(text: str) -> str:
    """Replace non-latin-1 characters so built-in Helvetica can render them."""
    for char, replacement in _UNICODE_REPLACEMENTS.items():
        text = text.replace(char, replacement)
    # Final fallback: encode to latin-1, replacing anything else with ?
    return text.encode("latin-1", errors="replace").decode("latin-1")

RETTS_RGB = {
    "RED":    (184, 65, 69),
    "ORANGE": (255, 135, 111),
    "YELLOW": (245, 154, 0),
    "GREEN":  (84, 185, 134),
    "BLUE":   (77, 181, 188),
}

_RETTS_TEXT_RGB = {
    "RED":    _WHITE,
    "ORANGE": _BLACK,
    "YELLOW": _BLACK,
    "GREEN":  _BLACK,
    "BLUE":   _BLACK,
}


