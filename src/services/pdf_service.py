class MedleyPDF(FPDF):
    """Base PDF class with Triage-Medley branding."""

    def __init__(self, report_type: str = "Triage Report"):
        super().__init__()
        self.report_type = report_type
        self.set_auto_page_break(auto=True, margin=20)

    def cell(self, *args, **kwargs):
        """Override to sanitize text for latin-1 font compatibility."""
        if args:
            args = list(args)
            for i, a in enumerate(args):
                if isinstance(a, str):
                    args[i] = _sanitize(a)
            args = tuple(args)
        for k, v in kwargs.items():
            if isinstance(v, str):
                kwargs[k] = _sanitize(v)
        return super().cell(*args, **kwargs)

    def multi_cell(self, *args, **kwargs):
        """Override to sanitize text for latin-1 font compatibility."""
        if args:
            args = list(args)
            for i, a in enumerate(args):
                if isinstance(a, str):
                    args[i] = _sanitize(a)
            args = tuple(args)
        for k, v in kwargs.items():
            if isinstance(v, str):
                kwargs[k] = _sanitize(v)
        return super().multi_cell(*args, **kwargs)

    def header(self):
        self.set_fill_color(*_HEADER_RGB)
        self.rect(0, 0, 210, 18, "F")
        self.set_font("Helvetica", "B", 14)
        self.set_text_color(*_WHITE)
        self.set_xy(10, 3)
        self.cell(0, 12, f"Triage-Medley  |  {self.report_type}", align="L")
        self.ln(20)

    def footer(self):
        self.set_y(-20)
        self.set_font("Helvetica", "I", 7)
        self.set_text_color(*_MUTED_RGB)
        self.cell(
            0, 5,
            f"Generated by Triage-Medley  |  "
            f"Page {self.page_no()}/{{nb}}  |  "
            f"{datetime.now().strftime('%Y-%m-%d %H:%M')}",
            align="C",
            new_x="LMARGIN", new_y="NEXT",
        )
        self.set_font("Helvetica", "", 6)
        self.cell(
            0, 5,
            "(c) 2026 SMAILE (Stockholm Medical AI and Learning Environments), "
            "Karolinska Institutet.",
            align="C",
        )

    # -- helpers ------------------------------------------------------------ #

    def section_heading(self, text: str) -> None:
        self.ln(2)
        self.set_font("Helvetica", "B", 12)
        self.set_text_color(*_HEADER_RGB)
        self.cell(0, 8, text, new_x="LMARGIN", new_y="NEXT")
        self.set_draw_color(*_HEADER_RGB)
        y = self.get_y()
        self.line(10, y, 200, y)
        self.ln(3)
        self.set_text_color(*_BLACK)

    def kv(self, key: str, value: str, bold_value: bool = False) -> None:
        """Key: value row with text wrapping for long values."""
        self.set_x(self.l_margin)  # Always start at left margin
        self.set_font("Helvetica", "B", 9)
        self.cell(50, 6, f"{key}:")
        self.set_font("Helvetica", "B" if bold_value else "", 9)
        # Use multi_cell for wrapping; temporarily shift left margin so
        # wrapped lines align with the value start
        old_l_margin = self.l_margin
        self.set_left_margin(old_l_margin + 50)
        self.multi_cell(0, 6, str(value))
        self.set_left_margin(old_l_margin)
        self.set_x(old_l_margin)  # Reset x for next element

    def retts_badge(self, level: str) -> None:
        """Inline colored RETTS level pill."""
        rgb = RETTS_RGB.get(level.upper(), _MUTED_RGB)
        text_rgb = _RETTS_TEXT_RGB.get(level.upper(), _BLACK)
        self.set_fill_color(*rgb)
        self.set_text_color(*text_rgb)
        self.set_font("Helvetica", "B", 10)
        w = self.get_string_width(level) + 10
        self.cell(w, 7, level, fill=True, align="C")
        self.set_text_color(*_BLACK)

    def bullet(self, text: str, indent: int = 4) -> None:
        """Single bullet point with text wrapping."""
        self.set_font("Helvetica", "", 9)
        old_l_margin = self.l_margin
        x_start = old_l_margin + indent
        self.set_x(x_start)
        self.set_left_margin(x_start + 6)  # Wrapped lines indent past "- "
        self.multi_cell(0, 5, f"- {text}")
        self.set_left_margin(old_l_margin)
        self.set_x(old_l_margin)  # Reset x for next element

    def alert_text(self, text: str) -> None:
        """Red alert text."""
        self.set_x(self.l_margin)  # Reset to left margin
        self.set_font("Helvetica", "B", 9)
        self.set_text_color(*_ERROR_RGB)
        self.multi_cell(0, 5, text)
        self.set_text_color(*_BLACK)


def _get_val(obj, attr, default=None):
    """Helper to get attribute or dict key robustly."""
    if obj is None: return default
    if isinstance(obj, dict): return obj.get(attr, default)
    return getattr(obj, attr, default)

