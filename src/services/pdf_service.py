def generate_triage_pdf(patient: PatientSession) -> bytes:
    """Generate triage report PDF. Returns bytes for st.download_button."""
    pdf = MedleyPDF("Triage Report")
    pdf.alias_nb_pages()
    pdf.add_page()

    _render_demographics(pdf, patient)
    _render_vitals(pdf, patient)
    _render_asr(pdf, patient)
    _render_ehr_risk_flags(pdf, patient)
    _render_triage_result(pdf, patient)
    _render_cross_engine(pdf, patient)
    _render_nurse_override(pdf, patient)

    # Timestamp
    pdf.ln(4)
    pdf.set_font("Helvetica", "I", 8)
    pdf.cell(
        0, 5,
        f"Report generated: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}",
        new_x="LMARGIN", new_y="NEXT",
    )

    return bytes(pdf.output())


def generate_physician_pdf(patient: PatientSession) -> bytes:
    """Generate physician report PDF. Returns bytes for st.download_button."""
    pdf = MedleyPDF("Physician Report")
    pdf.alias_nb_pages()
    pdf.add_page()

    # Shared sections (same as triage report)
    _render_demographics(pdf, patient)
    _render_vitals(pdf, patient)
    _render_asr(pdf, patient)
    _render_ehr_risk_flags(pdf, patient)
    _render_triage_result(pdf, patient)
    _render_cross_engine(pdf, patient)
    _render_nurse_override(pdf, patient)

    # Differential Diagnosis
    da = patient.differential_agreement
    if da:
        pdf.section_heading("Differential Diagnosis")

        all_agree = _get_val(da, "all_agree", [])
        if all_agree:
            pdf.set_font("Helvetica", "B", 10)
            pdf.set_x(pdf.l_margin)
            pdf.cell(0, 6, "Primary (Consensus - >=80% agreement):", new_x="LMARGIN", new_y="NEXT")
            for c in all_agree:
                prob_val = _get_val(c, "probability")
                prob = f"{prob_val:.0%}" if prob_val is not None else "N/A"
                dm = " [DON'T MISS]" if _get_val(c, "is_dont_miss", False) else ""
                old_lm = pdf.l_margin
                indent_x = old_lm + 4
                pdf.set_font("Helvetica", "", 9)
                pdf.set_x(indent_x)
                pdf.set_left_margin(indent_x)
                pdf.multi_cell(0, 5, f"{_get_val(c, 'diagnosis')} ({prob}){dm}")
                
                evidence = _get_val(c, "supporting_evidence", [])
                if evidence:
                    ev_indent = old_lm + 8
                    pdf.set_font("Helvetica", "I", 8)
                    pdf.set_text_color(*_MUTED_RGB)
                    evidence_text = "; ".join(evidence[:3])
                    pdf.set_x(ev_indent)
                    pdf.set_left_margin(ev_indent)
                    pdf.multi_cell(0, 4, f"Evidence: {evidence_text}")
                    pdf.set_text_color(*_BLACK)
                pdf.set_left_margin(old_lm)

        some_agree = _get_val(da, "some_agree", [])
        if some_agree:
            pdf.ln(2)
            pdf.set_font("Helvetica", "B", 10)
            pdf.set_x(pdf.l_margin)
            pdf.cell(0, 6, "Alternative (Partial - 40-79% agreement):", new_x="LMARGIN", new_y="NEXT")
            for c in some_agree:
                prob_val = _get_val(c, "probability")
                prob = f"{prob_val:.0%}" if prob_val is not None else "N/A"
                dm = " [DON'T MISS]" if _get_val(c, "is_dont_miss", False) else ""
                old_lm = pdf.l_margin
                indent_x = old_lm + 4
                pdf.set_font("Helvetica", "", 9)
                pdf.set_x(indent_x)
                pdf.set_left_margin(indent_x)
                pdf.multi_cell(0, 5, f"{_get_val(c, 'diagnosis')} ({prob}){dm}")
                pdf.set_left_margin(old_lm)

        devil = _get_val(da, "devil_advocate_only", [])
        if devil:
            pdf.ln(2)
            pdf.set_font("Helvetica", "B", 10)
            pdf.set_x(pdf.l_margin)
            pdf.cell(0, 6, "Minority (Devil's Advocate - <40%):", new_x="LMARGIN", new_y="NEXT")
            for c in devil:
                prob_val = _get_val(c, "probability")
                prob = f"{prob_val:.0%}" if prob_val is not None else "N/A"
                dm = " [DON'T MISS]" if _get_val(c, "is_dont_miss", False) else ""
                old_lm = pdf.l_margin
                indent_x = old_lm + 4
                pdf.set_font("Helvetica", "", 9)
                pdf.set_x(indent_x)
                pdf.set_left_margin(indent_x)
                pdf.multi_cell(0, 5, f"{_get_val(c, 'diagnosis')} ({prob}){dm}")
                pdf.set_left_margin(old_lm)

        dont_miss_all = _get_val(da, "dont_miss_all", [])
        if dont_miss_all:
            pdf.ln(2)
            pdf.set_font("Helvetica", "B", 9)
            pdf.set_text_color(*_ERROR_RGB)
            pdf.cell(0, 5, "Don't-Miss Diagnoses (any model):", new_x="LMARGIN", new_y="NEXT")
            pdf.set_text_color(*_BLACK)
            for dx in dont_miss_all:
                pdf.bullet(dx)
        pdf.ln(3)

    # Ensemble Management Recommendations
    ma = patient.management_agreement
    if ma:
        common_inv = _get_val(ma, "common_investigations", [])
        common_img = _get_val(ma, "common_imaging", [])
        common_med = _get_val(ma, "common_medications", [])
        has_consensus = common_inv or common_img or common_med

        if has_consensus:
            pdf.section_heading("Ensemble Management Recommendations")
            _render_management_category(pdf, "Investigations (Consensus)", common_inv)
            _render_management_category(pdf, "Imaging (Consensus)", common_img)
            _render_management_category(pdf, "Medications (Consensus)", common_med)
        else:
            pdf.section_heading("Management Recommendations")
            pdf.set_font("Helvetica", "I", 9)
            pdf.set_text_color(*_MUTED_RGB)
            pdf.multi_cell(0, 5, "No consensus across models. Individual model recommendations shown below.")
            pdf.set_text_color(*_BLACK)
            pdf.ln(2)

        # Disposition votes
        pdf.set_font("Helvetica", "B", 9)
        pdf.cell(0, 6, "Disposition:", new_x="LMARGIN", new_y="NEXT")
        cons_disp = _get_val(ma, "consensus_disposition")
        if cons_disp:
            pdf.set_font("Helvetica", "", 9)
            pdf.cell(0, 5, f"  Consensus: {str(cons_disp).title()}", new_x="LMARGIN", new_y="NEXT")
        
        disp_votes = _get_val(ma, "disposition_votes", {})
        if disp_votes:
            pdf.set_font("Helvetica", "I", 8)
            pdf.set_text_color(*_MUTED_RGB)
            votes_str = ", ".join(f"{str(d).title()}: {c}" for d, c in disp_votes.items())
            pdf.cell(0, 5, f"  Votes: {votes_str}", new_x="LMARGIN", new_y="NEXT")
            pdf.set_text_color(*_BLACK)

        # Individual model items
        _render_individual_model_items(pdf, patient, promoted=not has_consensus)

        # Contraindications
        contra = _get_val(ma, "contraindications", [])
        if contra:
            pdf.ln(2)
            pdf.set_font("Helvetica", "B", 9)
            pdf.set_text_color(*_ERROR_RGB)
            pdf.cell(0, 5, "Contraindications Flagged:", new_x="LMARGIN", new_y="NEXT")
            pdf.set_text_color(*_BLACK)
            for ci in contra:
                pdf.bullet(ci)
        pdf.ln(3)

    # Approved Management Plan (physician selections)
    if patient.is_signed_off or patient.physician_approved_investigations or patient.physician_approved_imaging or patient.physician_approved_medications:
        pdf.section_heading("Physician-Approved Plan")
        _render_approved_list(pdf, "Investigations", patient.physician_approved_investigations)
        _render_approved_list(pdf, "Imaging", patient.physician_approved_imaging)
        _render_approved_list(pdf, "Medications", patient.physician_approved_medications)
        pdf.kv("Disposition", (patient.physician_disposition or "Not specified").title())
        pdf.ln(3)

    # Physician Notes
    if patient.physician_notes:
        pdf.section_heading("Physician Notes")
        pdf.set_font("Helvetica", "", 9)
        pdf.multi_cell(0, 5, patient.physician_notes)
        pdf.ln(3)

    # Sign-off
    pdf.section_heading("Physician Sign-Off")
    if patient.is_signed_off:
        pdf.kv("Status", "SIGNED OFF", bold_value=True)
        pdf.kv("Physician", patient.physician_name or "Unknown")
        pdf.kv(
            "Signed at",
            patient.physician_sign_off.strftime("%Y-%m-%d %H:%M:%S"),
        )
    else:
        pdf.set_font("Helvetica", "B", 10)
        pdf.set_text_color(*_ERROR_RGB)
        pdf.cell(0, 7, "DRAFT - PENDING SIGN-OFF", new_x="LMARGIN", new_y="NEXT")
        pdf.set_text_color(*_BLACK)

    # Timestamp
    pdf.ln(4)
    pdf.set_font("Helvetica", "I", 8)
    pdf.cell(
        0, 5,
        f"Report generated: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}",
        new_x="LMARGIN", new_y="NEXT",
    )

    return bytes(pdf.output())
